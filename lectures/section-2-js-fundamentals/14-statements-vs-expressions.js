// Statements vs Expressions

// Expressions/Operator evaluate to a VALUE.

42; // Numeric literal
("hello"); // String literal
true; // Boolean literal

// Statements perform ACTIONS.
// Statements control the flow of execution, perform operations, and structure the program.

if (a > b) {
  console.log("a is greater than b");
} else if (a < b) {
  console.log("a is less than b");
} else {
  console.log("a is equal to b");
}

// Example only {Expressions} can be placed when using template literals
console.log(`I'm ${2024 - 1984} years old!`);
